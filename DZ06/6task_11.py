"""
Дан список значений. Превратить список в словарь где
ключами служат элементы списка, а значениями квадраты этих
элементов. [1,2,3] -> {1:1, 2:4, 3:9}
"""

# a = [1, 2, 3]

def list_to_dict(a):
    # b = dict([[i, i ** 2] for i in a])
    b = {key: key ** 2 for key in a}
    return b

# print(b)

if __name__ == '__main__':
    assert list_to_dict([1, 2, 3]) == {1: 1, 2: 4, 3: 9}